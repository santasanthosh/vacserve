<h2>Solutions to Land Pollution</h2>
There are several possible solutions to land pollution, including conservation. Conservation focuses on preserving natural resources, such as soil and plants.
<br> The efforts to conserve resources can start with utilizing sustainable practices.<br>

For instance, leaving some of the trees in a forest to naturally die and decay. This not only leaves the cover needed for the soil and other vegetation, but it helps to 
<br>provide the nutrients that the soil needs to remain fertile. Other solutions include:<br>

Proper waste disposal that focuses on treating waste and disposing it in the safest manner possible.<br>
Reusing materials to reduce the need for harvesting of resources. Products that are not reusable can likely be recycled.<br>
Reducing the usage of non-biodegradable materials, such as plastic shopping bags. The simple act of switching to a reusable cloth bag for groceries can help 
<br>cut down on the need for non-biodegradable materials.<br>
Organic gardening can reduce the usage of pesticides and insecticides. Non-gardeners can help by buying organic food.<br>
