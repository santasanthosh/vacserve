<br>to the environment and animals, including these top two:</h4><br>

<h3>1. Ground water poisoning</h3><br>
Depending on the soil and whether the chemicals were improperly disposed of on the land, the chemicals could end up in the ground water. The process is known as leaching. 
<br>It can occur on farms, industrial sites, and landfills.<br>

