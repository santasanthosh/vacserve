<h3>2. Agricultural chemicals</h3>
<br>
Part of the farming process often involves the use of harmful pesticides and insecticides to protect crops. However, the chemicals can cause the land to become barren.
<br> The once-fertile soil is then more susceptible to environmental elements, such as the wind.<br>

<br>


<h1>Effects of Land Pollution</h1>
<br>
