<h1>Effects of Land Pollution on Humans</h1>
The impact of land pollution is not limited just to the earth and animals. Humans can also experience negative consequences that can influence quality of life and health.<br>

Some of the potential consequences include birth defects, the development of breathing disorders, skin diseases, and cancer. Most of these develop after exposure to waste
<br> from water poisoning and soil contamination.<br>

Land pollution has also been linked to developmental deficits in children. Chemicals that are commonly found in contaminated soil and water, such as lead, have can impact 
<br>a child’s cognitive development even if the exposure is very low.<br>

