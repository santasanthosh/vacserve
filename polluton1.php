<h1>introduction</h1>
<p></p><h3>pollution is the process of making land, water, air or other parts of the environment dirty and not safe// or suitable to use. This can be done through the introduction of a
 contaminant into a natural environment, but the contaminant doesn't need to be tangible. Things as simple as light, sound and temperature can be considered pollutants when 
 introduced artificially into an environment. 

Toxic pollution affects more than 200 million people worldwide, according to Pure Earth, a non-profit environmental organization. In some of the world's worst polluted places, 
babies are born with birth defects, children have lost 30 to 40 IQ points, and life expectancy may be as low as 45 years because of cancers and other diseases.</h3><p></p>

