 <h2><u>water pollution</u></h2>
 <p>British poet W. H. Auden once noted, “Thousands have lived without love, not one without water.” Yet while we all know water is crucial for life, we trash it 
 anyway. Some 80 percent of the world’s wastewater is dumped—largely untreated—back into the environment, polluting rivers, lakes, and oceans.<br>

This widespread problem of water pollution is jeopardizing our health. Unsafe water kills more people each year than war and all other forms of violence combined.
<br> Meanwhile, our drinkable water sources are finite: Less than 1 percent of the earth’s freshwater is actually accessible to us. Without action, the challenges 
<br>will only increase by 2050, when global demand for freshwater is expected to be one-third greater than it is now.

Sip a glass of cool, clear water as you read this, and you may think water pollution is a problem . . . somewhere else. But while most Americans have access to safe drinking water
<br>, potentially harmful contaminants—from arsenic to copper to lead—have been found in the tap water of every single state in the nation.

Still, we’re not hopeless against the threat to clean water. To better understand the problem and what we can do about it, here’s an overview of 
<br>what water pollution is, what causes it, and how we can protect ourselves.
<br>
<br>
<br>
</p><h1>What Is Water Pollution?</h1>
Water pollution occurs when harmful substances—often chemicals or microorganisms—contaminate a stream, river, lake, ocean, aquifer, or other body of water, degrading water quality 
<br>and rendering it toxic to humans or the environment.<br>

<h1>What Are the Causes of Water Pollution?</h1>
Water is uniquely vulnerable to pollution. Known as a “universal solvent,” water is able to dissolve more substances than any other liquid on earth. It’s the reason we have
<br> Kool-Aid and brilliant blue waterfalls. It’s also why water is so easily polluted. Toxic substances from farms, towns, and factories readily dissolve into and mix with it,
<br> causing water pollution.<br>

<h1>Categories of Water Pollution</h1>
<u>Groundwater</u>
When rain falls and seeps deep into the earth, filling the cracks, crevices, and porous spaces of an aquifer (basically an underground storehouse of water), it becomes 
<br>groundwater—one of our least visible but most important natural resources. Nearly 40 percent of Americans rely on groundwater, pumped to the earth’s surface, for drinking water.
<br> For some folks in rural areas, it’s their only freshwater source. Groundwater gets polluted when contaminants—from pesticides and fertilizers to waste leached from landfills
<br> and septic systems—make their way into an aquifer, rendering it unsafe for human use. Ridding groundwater of contaminants can be difficult to impossible, as well as costly. 
<br>Once polluted, an aquifer may be unusable for decades, or even thousands of years. Groundwater can also spread contamination far from the original polluting source as it seeps 

<br>into streams, lakes, and oceans.
<br>
<br>
<b><u>Surface water</u></b>
Covering about 70 percent of the earth, surface water is what fills our oceans, lakes, rivers, and all those other blue bits on the world map. Surface water from freshwater sources 
<br>(that is, from sources other than the ocean) accounts for more than 60 percent of the water delivered to American homes. But a significant pool of that water is in peril.
<br> According to the most recent surveys on national water quality from the U.S. Environmental Protection Agency, nearly half of our rivers and streams and more than one-third 
<br>of our lakes are polluted and unfit for swimming, fishing, and drinking. Nutrient pollution, which includes nitrates and phosphates, is the leading type of contamination in
<br> these freshwater sources. While plants and animals need these nutrients to grow, they have become a major pollutant due to farm waste and fertilizer runoff. Municipal and <br>
industrial waste discharges contribute their fair share of toxins as well. There’s also all the random junk that industry and individuals dump directly into waterways.<br>

