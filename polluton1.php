<h2>air pollution</h2>
<br>
Air pollution is a mix of particles and gases that can reach harmful concentrations both outside and indoors. Its effects can range from higher disease risks to rising
<br<>temperatures. Soot, smoke, mold, pollen, methane, and carbon dioxide are a just few examples of common pollutants.<br>


In the U.S., one measure of outdoor air pollution is the Air Quality Index, or AQI which rates air conditions across the country based on concentrations of five major
<br> pollutants: ground-level ozone, particle pollution (or particulate matter), carbon monoxide, sulfur dioxide, and nitrogen dioxide. Some of those also contribute to 
<br>indoor air pollution, along with radon, cigarette smoke, volatile organic compounds (VOCs), formaldehyde, asbestos, and other substances.<br>
